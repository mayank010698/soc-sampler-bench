"""Aggregate every results/*.json into one markdown table.

Run after any run_benchmark.py invocation -- never edit LEADERBOARD.md by hand.
"""
import json
from pathlib import Path

RESULTS_DIR = Path(__file__).resolve().parents[1] / "results"
OUT = Path(__file__).resolve().parents[1] / "LEADERBOARD.md"

COLUMNS = ["method", "target", "dim", "nfe", "train_steps", "seed",
           "sinkhorn", "ess", "log_Z_estimate", "mode_discrepancy", "train_wallclock_s"]


def _fmt(v):
    if v is None:
        return "-"
    if isinstance(v, float):
        return f"{v:.4g}"
    return str(v)


def main():
    rows = [json.loads(f.read_text()) for f in sorted(RESULTS_DIR.glob("*.json"))]

    lines = ["| " + " | ".join(COLUMNS) + " |",
             "|" + "|".join(["---"] * len(COLUMNS)) + "|"]
    for r in rows:
        lines.append("| " + " | ".join(_fmt(r.get(c)) for c in COLUMNS) + " |")

    OUT.write_text(
        "# Leaderboard\n\n_Generated by `make_leaderboard.py` -- do not edit by hand._\n\n"
        + "\n".join(lines) + "\n"
    )
    print(f"Wrote {OUT} ({len(rows)} rows)")


if __name__ == "__main__":
    main()
